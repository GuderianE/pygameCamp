def buildWorldMatrix(width, height, blocks):
    world_map = [[0 for _ in range(width)] for _ in range(height)]
    
    for x in range(width):
        world_map[0][x] = 1 
        world_map[height - 1][x] = 2
    
    for y in range(height):
        world_map[y][0] = 1
        world_map[y][width - 1] = 1 
    
    for block, x, y in blocks:
        if 0 <= x < width and 0 <= y < height:
            world_map[y][x] = block  # Left side
        
        
    
    return world_map

